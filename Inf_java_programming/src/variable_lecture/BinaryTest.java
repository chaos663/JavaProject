package variable_lecture;

public class BinaryTest {

	public static void main(String[] args) {
		// 10진수
		int num = 10;
		// 2진수(0B로 시작)
		int bNum = 0B1010;
		// 8진수 0으로 시작
		int oNum = 012;
		// 16진수(0X러 시작)
		int hNum = 0XA;
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}
}
