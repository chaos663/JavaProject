package org.honey.javatutorials.variables;

public class IntergerDemo {

	public static void main(String[] args) {
		// 데이터의 자료형 선언, 즉, 변수 a의 type을 선언 하는데 이를 DataType을 강제한다라고 한다.
		int a;
		// 변수에 값을 할당
		a = 1;
		System.out.println(a + 1);
		// 변수에 값을 할당
		a = 2;
		System.out.println(a + 1);

	}

}
